"use client";

import { Stock } from "@prisma/client";
import { StockListContext } from "./StockListContext";
import { ReactNode } from "react";

function StockListProvider({
  stockList,
  children,
}: {
  stockList: Stock[];
  children: ReactNode;
}) {
  return (
    <StockListContext.Provider value={stockList}>
      {children}
    </StockListContext.Provider>
  );
}

export default StockListProvider;
